void main() {
  // Arithmetic operators
  int a = 15;
  int b = 4;

  int sum = a + b;
  int remainder = a % b;

  print("Sum: $sum");
  print("Remainder: $remainder");

  // Logical operators
  bool hasLaptop = true;
  bool hasInternet = false;

  bool canFreelance = hasLaptop && hasInternet;

  print("Can Freelance: $canFreelance");
}